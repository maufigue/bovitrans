-- ─────────────────────────────────────────
-- TABLA: trucks
-- ─────────────────────────────────────────

CREATE TABLE IF NOT EXISTS trucks (
  id                      UUID          PRIMARY KEY DEFAULT gen_random_uuid(),
  license_plate           VARCHAR(20)   NOT NULL,
  brand                   VARCHAR(80)   NOT NULL DEFAULT 'Genérico',
  model                   VARCHAR(120)  NOT NULL DEFAULT 'Camión ganadero',
  engine_power_hp         INTEGER       NULL,
  tare_weight_tons        DECIMAL(6,2)  NOT NULL DEFAULT 9.00,
  empty_fuel_consumption_per_km DECIMAL(7,4) NOT NULL DEFAULT 0.3200,
  fuel_consumption_per_ton_km DECIMAL(7,4) NOT NULL DEFAULT 0.0065,
  max_capacity            INTEGER       NOT NULL,
  vehicle_configuration   VARCHAR(40)   NOT NULL DEFAULT 'truck_semitrailer',
  axle_configuration      VARCHAR(40)   NOT NULL DEFAULT 'double_dual',
  length_m                DECIMAL(5,2)  NOT NULL DEFAULT 22.40,
  max_weight_tons         DECIMAL(6,2)  NOT NULL DEFAULT 18.00,
  reference_cattle_weight_kg INTEGER     NOT NULL DEFAULT 450,
  fuel_consumption_per_km DECIMAL(6,3)  NOT NULL,
  status                  truck_status  NOT NULL DEFAULT 'available',
  created_at              TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
  updated_at              TIMESTAMPTZ   NOT NULL DEFAULT NOW(),

  CONSTRAINT trucks_license_plate_unique   UNIQUE (license_plate),
  CONSTRAINT trucks_engine_power_positive CHECK (engine_power_hp IS NULL OR engine_power_hp > 0),
  CONSTRAINT trucks_tare_weight_positive CHECK (tare_weight_tons > 0),
  CONSTRAINT trucks_empty_fuel_positive CHECK (empty_fuel_consumption_per_km > 0),
  CONSTRAINT trucks_fuel_ton_positive CHECK (fuel_consumption_per_ton_km > 0),
  CONSTRAINT trucks_max_capacity_positive  CHECK  (max_capacity > 0),
  CONSTRAINT trucks_length_positive CHECK (length_m > 0),
  CONSTRAINT trucks_max_weight_positive CHECK (max_weight_tons > 0),
  CONSTRAINT trucks_reference_cattle_weight_positive CHECK (reference_cattle_weight_kg > 0),
  CONSTRAINT trucks_vehicle_configuration_check CHECK (
    vehicle_configuration IN (
      'simple_truck', 'truck_with_trailer', 'trailer',
      'truck_semitrailer', 'truck_semitrailer_trailer'
    )
  ),
  CONSTRAINT trucks_axle_configuration_check CHECK (
    axle_configuration IN (
      'single_single', 'single_dual', 'double_single',
      'double_dual_single', 'double_dual',
      'triple_dual_single', 'triple_dual'
    )
  ),
  CONSTRAINT trucks_fuel_consumption_positive CHECK (fuel_consumption_per_km > 0)
);

