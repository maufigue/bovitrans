-- ─────────────────────────────────────────
-- FUNCIÓN: auto-actualizar updated_at
-- ─────────────────────────────────────────

CREATE OR REPLACE FUNCTION trigger_set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS set_updated_at_trucks ON trucks;
CREATE TRIGGER set_updated_at_trucks
  BEFORE UPDATE ON trucks
  FOR EACH ROW EXECUTE FUNCTION trigger_set_updated_at();

DROP TRIGGER IF EXISTS set_updated_at_app_users ON app_users;
CREATE TRIGGER set_updated_at_app_users
  BEFORE UPDATE ON app_users
  FOR EACH ROW EXECUTE FUNCTION trigger_set_updated_at();

DROP TRIGGER IF EXISTS set_updated_at_clients ON clients;
CREATE TRIGGER set_updated_at_clients
  BEFORE UPDATE ON clients
  FOR EACH ROW EXECUTE FUNCTION trigger_set_updated_at();

DROP TRIGGER IF EXISTS set_updated_at_transport_requests ON transport_requests;
CREATE TRIGGER set_updated_at_transport_requests
  BEFORE UPDATE ON transport_requests
  FOR EACH ROW EXECUTE FUNCTION trigger_set_updated_at();

