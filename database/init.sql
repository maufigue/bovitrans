-- ─────────────────────────────────────────
-- TABLA: transport_requests
-- ─────────────────────────────────────────

CREATE TABLE IF NOT EXISTS transport_requests (
  id               UUID            PRIMARY KEY DEFAULT gen_random_uuid(),

  -- Datos del cliente
  client_id        UUID            NULL
                   REFERENCES clients(id)
                   ON DELETE RESTRICT
                   ON UPDATE CASCADE,
  client_name      VARCHAR(120)    NOT NULL,
  cattle_count     INTEGER         NOT NULL,
  cattle_weight_min_kg INTEGER     NOT NULL DEFAULT 400,
  cattle_weight_max_kg INTEGER     NOT NULL DEFAULT 500,

  -- Origen
  origin_name      VARCHAR(200)    NOT NULL,
  origin_lat       DECIMAL(10,7)   NOT NULL,
  origin_lng       DECIMAL(10,7)   NOT NULL,

  -- Destino
  destination_name VARCHAR(200)    NOT NULL,
  destination_lat  DECIMAL(10,7)   NOT NULL,
  destination_lng  DECIMAL(10,7)   NOT NULL,

  -- Logística calculada (se persiste al asignar camión)
  distance_km      DECIMAL(10,3)   NULL,
  fuel_cost        DECIMAL(12,2)   NULL,
  fuel_price_id    UUID            NULL,
  trips_needed     INTEGER         NULL DEFAULT 1,
  departure_at     TIMESTAMPTZ     NULL,
  estimated_arrival_at TIMESTAMPTZ NULL,

  -- Relaciones y estado
  status           request_status  NOT NULL DEFAULT 'pending',
  truck_id         UUID            NULL
                   REFERENCES trucks(id)
                   ON DELETE RESTRICT
                   ON UPDATE CASCADE,

  notes            TEXT            NULL,
  source           VARCHAR(20)     NOT NULL DEFAULT 'internal',
  route_pending    BOOLEAN         NOT NULL DEFAULT FALSE,
  created_at       TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
  updated_at       TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

  CONSTRAINT tr_cattle_count_positive CHECK (cattle_count > 0),
  CONSTRAINT tr_cattle_weight_min_positive CHECK (cattle_weight_min_kg > 0),
  CONSTRAINT tr_cattle_weight_max_positive CHECK (cattle_weight_max_kg > 0),
  CONSTRAINT tr_cattle_weight_range_valid CHECK (cattle_weight_min_kg <= cattle_weight_max_kg),
  CONSTRAINT tr_trips_needed_positive CHECK (trips_needed > 0),
  CONSTRAINT tr_origin_dest_different CHECK (
    origin_lat <> destination_lat OR origin_lng <> destination_lng
  ),
  CONSTRAINT tr_fuel_cost_requires_truck CHECK (
    fuel_cost IS NULL OR truck_id IS NOT NULL
  ),
  CONSTRAINT tr_source_check CHECK (source IN ('internal', 'external'))
);

-- Índices
CREATE INDEX IF NOT EXISTS idx_tr_status     ON transport_requests (status);
CREATE INDEX IF NOT EXISTS idx_tr_truck_id   ON transport_requests (truck_id);
CREATE INDEX IF NOT EXISTS idx_tr_client_id  ON transport_requests (client_id);
CREATE INDEX IF NOT EXISTS idx_tr_created_at ON transport_requests (created_at DESC);

-- Tabla puente para asignar uno o varios camiones a una solicitud.
-- transport_requests.truck_id conserva el camion principal para compatibilidad.
CREATE TABLE IF NOT EXISTS transport_request_trucks (
  transport_request_id UUID NOT NULL
    REFERENCES transport_requests(id)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
  truck_id UUID NOT NULL
    REFERENCES trucks(id)
    ON DELETE RESTRICT
    ON UPDATE CASCADE,
  sequence INTEGER NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  CONSTRAINT trt_pk PRIMARY KEY (transport_request_id, truck_id),
  CONSTRAINT trt_sequence_positive CHECK (sequence > 0),
  CONSTRAINT trt_request_sequence_unique UNIQUE (transport_request_id, sequence)
);

CREATE INDEX IF NOT EXISTS idx_trt_truck_id ON transport_request_trucks (truck_id);

